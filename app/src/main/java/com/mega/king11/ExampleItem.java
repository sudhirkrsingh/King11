package com.mega.king11;

public class ExampleItem {
    String mSeriesName;
    String uId,Id;

    public ExampleItem() {
    }

    public ExampleItem(String mSeriesName, String uId, String id) {
        this.mSeriesName = mSeriesName;
        this.uId = uId;
        Id = id;
    }

    public String getmSeriesName() {
        return mSeriesName;
    }

    public String getuId() {
        return uId;
    }

    public String getId() {
        return Id;
    }
}
